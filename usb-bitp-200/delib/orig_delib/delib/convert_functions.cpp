// ----------------------------------------------------------------------------

unsigned long convert_hex_to_ascii(unsigned long wert)
{
	unsigned long ret=0;
	wert&=0x0f;

	if(wert>9) ret=wert-10+'A';
	else ret=wert+'0';

	return ret;
}

