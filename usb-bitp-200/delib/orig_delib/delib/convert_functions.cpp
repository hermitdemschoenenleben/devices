unsigned long convert_ascii_to_hex(unsigned long hex)
{
	unsigned long ret=0;
	if(hex>='0' && hex<='9') ret=hex-'0';
	if(hex>='A' && hex<='F') ret=hex-'A'+10;
	if(hex>='a' && hex<='f') ret=hex-'a'+10;

	return ret;
}

// ----------------------------------------------------------------------------
// ----------------------------------------------------------------------------
